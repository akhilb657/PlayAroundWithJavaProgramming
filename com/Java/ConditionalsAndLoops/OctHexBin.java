package com.Java.ConditionalsAndLoops;

public class OctHexBin {
	public static void main(String[] args) {
		int i = 045;//octal number , corresponding decimal will be given to i
		System.out.println(i);
		
		int j=0x45;//hexaDecimal number , corresponding decimal will be given to i
		System.out.println(j);
		
		int b = 0b10101;//binary number , corresponding decimal will be given to i
		System.out.println(b);
	}

}
