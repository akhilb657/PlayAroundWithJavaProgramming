package com.Java.ConditionalsAndLoops;

public class SwitchCase {
	public static void main(String[] args) {
		int exp = 300;
		
		switch(exp){
			case 100:
				System.out.println("Case 100 satisfied");
				break;
			case 200:
				System.out.println("Case 200 satisfied");
				break;
			case 300:
				System.out.println("Case 300 satisfied");
				break;
			default:
				System.out.println("None of the above");
				
		}
	}

}
